// ユーザー
Route::namespace('Users')->prefix('users')->name('users.')->group(function () {

    // ログイン認証関連
    Auth::routes([
        'register' => true,
        'reset'    => false,
        'verify'   => false
    ]);

    // ログイン認証後
    Route::middleware('auth:users')->group(function () {

        // TOPページ
        Route::get('home', [App\Http\Controllers\Users\HomeController::class, 'index']);
    });
});

